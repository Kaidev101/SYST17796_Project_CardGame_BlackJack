package ca.sheridancollege.project;

/**
 * @author Group Not Done
 */

public class PlayingCard extends Card {

    private String suit;
    private String rank;
    private int value;

    public PlayingCard(String suit, String rank, int value) {
        this.suit = suit;
        this.rank = rank;
        this.value = value;
    }

    public int getValue() {
        return value;
    }

    public String getSuit() {
        return suit;
    }

    public String getRank() {
        return rank;
    }

    @Override
    public String toString() {
        return rank + " of " + suit;
    }
}


